private void khoiTaoTable() {
    String[] columnNames = {"Mã SP", "Tên sản phẩm", "Đơn giá", "Tồn kho"};
    tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Không cho edit trực tiếp trên table
        }
    };
    tblSanPham.setModel(tableModel);
    
    // Thiết lập độ rộng cột
    tblSanPham.getColumnModel().getColumn(0).setPreferredWidth(60);
    tblSanPham.getColumnModel().getColumn(1).setPreferredWidth(200);
    tblSanPham.getColumnModel().getColumn(2).setPreferredWidth(100);
    tblSanPham.getColumnModel().getColumn(3).setPreferredWidth(80);
    
    // Thêm sự kiện click vào row
    tblSanPham.addMouseListener(new java.awt.event.MouseAdapter() {
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            hienThiThongTinSanPham();
        }
    });
}

private void loadData() {
    tableModel.setRowCount(0); // Xóa dữ liệu cũ
    List<SanPham> list = sanPhamDAO.getAllSanPham();
    
    for (SanPham sp : list) {
        Object[] row = {
            sp.getMaSP(),
            sp.getTenSP(),
            String.format("%,.0f", sp.getDonGia()),
            sp.getTonKho()
        };
        tableModel.addRow(row);
    }
}

private void hienThiThongTinSanPham() {
    int selectedRow = tblSanPham.getSelectedRow();
    if (selectedRow >= 0) {
        txtMaSP.setText(tableModel.getValueAt(selectedRow, 0).toString());
        txtTenSP.setText(tableModel.getValueAt(selectedRow, 1).toString());
        
        // Xóa dấu phẩy trong đơn giá
        String donGia = tableModel.getValueAt(selectedRow, 2).toString().replace(",", "");
        txtDonGia.setText(donGia);
        
        txtTonKho.setText(tableModel.getValueAt(selectedRow, 3).toString());
    }
}

private void xoaForm() {
    txtMaSP.setText("");
    txtTenSP.setText("");
    txtDonGia.setText("");
    txtTonKho.setText("");
    tblSanPham.clearSelection();
}

private boolean validateForm() {
    if (txtTenSP.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập tên sản phẩm!");
        txtTenSP.requestFocus();
        return false;
    }
    
    try {
        double donGia = Double.parseDouble(txtDonGia.getText().trim());
        if (donGia <= 0) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải lớn hơn 0!");
            txtDonGia.requestFocus();
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Đơn giá không hợp lệ!");
        txtDonGia.requestFocus();
        return false;
    }
    
    try {
        int tonKho = Integer.parseInt(txtTonKho.getText().trim());
        if (tonKho < 0) {
            JOptionPane.showMessageDialog(this, "Tồn kho không được âm!");
            txtTonKho.requestFocus();
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Tồn kho không hợp lệ!");
        txtTonKho.requestFocus();
        return false;
    }
    
    return true;
}
private void khoiTaoTableKH() {
    String[] columnNames = {"Mã KH", "Tên khách hàng", "Số điện thoại", "Địa chỉ"};
    tableModelKH = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    tblKhachHang.setModel(tableModelKH);
    
    // Thiết lập độ rộng cột
    tblKhachHang.getColumnModel().getColumn(0).setPreferredWidth(60);
    tblKhachHang.getColumnModel().getColumn(1).setPreferredWidth(150);
    tblKhachHang.getColumnModel().getColumn(2).setPreferredWidth(100);
    tblKhachHang.getColumnModel().getColumn(3).setPreferredWidth(200);
    
    // Thêm sự kiện click vào row
    tblKhachHang.addMouseListener(new java.awt.event.MouseAdapter() {
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            hienThiThongTinKhachHang();
        }
    });
}

private void loadDataKH() {
    tableModelKH.setRowCount(0);
    List<KhachHang> list = khachHangDAO.getAllKhachHang();
    
    for (KhachHang kh : list) {
        Object[] row = {
            kh.getMaKH(),
            kh.getTenKH(),
            kh.getSdt(),
            kh.getDiaChi()
        };
        tableModelKH.addRow(row);
    }
}

private void hienThiThongTinKhachHang() {
    int selectedRow = tblKhachHang.getSelectedRow();
    if (selectedRow >= 0) {
        txtMaKH.setText(tableModelKH.getValueAt(selectedRow, 0).toString());
        txtTenKH.setText(tableModelKH.getValueAt(selectedRow, 1).toString());
        
        Object sdt = tableModelKH.getValueAt(selectedRow, 2);
        txtSDT.setText(sdt != null ? sdt.toString() : "");
        
        Object diaChi = tableModelKH.getValueAt(selectedRow, 3);
        txtDiaChi.setText(diaChi != null ? diaChi.toString() : "");
    }
}

private void xoaFormKH() {
    txtMaKH.setText("");
    txtTenKH.setText("");
    txtSDT.setText("");
    txtDiaChi.setText("");
    tblKhachHang.clearSelection();
}

private boolean validateFormKH() {
    if (txtTenKH.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập tên khách hàng!");
        txtTenKH.requestFocus();
        return false;
    }
    
    String sdt = txtSDT.getText().trim();
    if (!sdt.isEmpty() && !sdt.matches("^0\\d{9,10}$")) {
        JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ! (phải bắt đầu bằng 0 và có 10-11 số)");
        txtSDT.requestFocus();
        return false;
    }
    
    return true;
}
